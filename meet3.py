# TIPE DATA 
#  (tipe dari sebuah nilai)

# tpe data integer
x = 10
print("tipe data dari x =",type(x))

# tipe data float
y = 5.5
print("tipe data dari y =",type(y))

# tipe data string
nama = "Raden wira"
print("tipe data dari nama =", type(nama))

# tipe data bolean

kebenaran = True
print("nilai dari kebenaran=", type (kebenaran))
print("tipe data dari kebenaran=", type (kebenaran))

