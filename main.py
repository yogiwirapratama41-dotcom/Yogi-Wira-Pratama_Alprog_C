# Jika ingin mengambil data INTEGER, maka contohnya seperti di bawah ini

Data_int = int (input ("Masukkan data Anda : "))
print("Nilai Anda =", Data_int)
print(type(Data_int))

# Jika ingin mengambil data FLOAT, maka contohnya seperti di bawah ini

Data_float = float (input ("Masukkan data Anda : "))
print("Nilai Anda =", Data_float)
print(type(Data_float))

# Jika ingin mengambil data BLOOEAN, maka contohnya seperti di bawah ini

Data_bool = bool (input ("Masukkan data Anda : "))
